package org.openehr.rm.support;

import org.openehr.rm.support.measurement.MeasurementService;
import org.openehr.rm.support.terminology.TerminologyService;

/**
 * A mixin class providing access to services in the external environment
 *
 * @author Yin Su Lim
 * @version 1.0
 */
public interface ExternalEnvironment {
	
	/**
	 * Return an interface to the terminology servive
	 * 
	 * @return terminology service not null
	 */
      public TerminologyService terminologyService();
      
      
      /**
       * Return an interface to the measurement service
       * 
       * @return measurement service not null
       */
      public MeasurementService measurementService();
      
}

